'''
--- Part Two ---

Ding! The "fasten seat belt" signs have turned on. Time to find your seat.

It's a completely full flight, so your seat should be the only missing boarding pass in your list. However, there's a catch: some of the seats at the very front and back of the plane don't exist on this aircraft, so they'll be missing from your list as well.

Your seat wasn't at the very front or back, though; the seats with IDs +1 and -1 from yours will be in your list.

What is the ID of your seat?

'''
def go_to(min, max, upper):
    if upper:
        return min, (min + max)//2
    else:
        return (min + max)//2 +1, max

def find_position(path):
    row_min, row_max = 0,127
    for direction in path[:7]:
        upper = True if direction == 'F' else False
        row_min, row_max = go_to(row_min, row_max, upper)

    col_min, col_max = 0, 7
    for direction in path[7:]:
        upper = True if direction == 'L' else False
        col_min, col_max = go_to(col_min, col_max, upper)

    seat_id = row_min * 8 + col_min
    return seat_id


def get_all_seat_id(input:str):
    return [
            find_position(path)
            for path in input.strip().splitlines()
        ]
    

all_seats = sorted(get_all_seat_id(input))

